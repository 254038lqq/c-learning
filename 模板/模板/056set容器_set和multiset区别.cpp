#include <set>
#include<iostream>
#include<string>

using namespace std;

//set和multiset区别
void test05601()
{
	set<int> s;
	//- set插入数据的同时会返回插入结果，表示插入是否成功
	//pair  对组  第一个为返回的迭代器  第二个值为 一个 bool 值 表示是否插入成功
	pair<set<int>::iterator, bool> ret = s.insert(10);
	if (ret.second) {
		cout << "第一次插入成功!" << endl;
	}
	else {
		cout << "第一次插入失败!" << endl;
	}
	ret = s.insert(10);
	if (ret.second) {
		cout << "第二次插入成功!" << endl;
	}
	else {
		cout << "第二次插入失败!" << endl;
	}

	//multiset
	multiset<int> ms;
	ms.insert(10);
	ms.insert(10);
	for (multiset<int>::iterator it = ms.begin(); it != ms.end(); it++) {
		cout << *it << " ";
	}
	cout << endl;
}
int main056() {
	test05601();
	system("pause");
	return 0;
}