#include<iostream>
#include<string>
using namespace std;
//模板的局限性
//模板的通用性并不是万能的
//有些特定数据类型，需要用具体化方式做特殊实现

//普通函数模板
//对比两个数据是否相等的模板
template<class T>
bool mycompare006(T &a, T &b)
{
    if (a = b) {
        return true;
    } 
    else {
        return false;
    }
}
void test00601() {
    int a = 10;
    int b = 10;
    bool ret = mycompare006(a, b);
    if (ret) {
        cout << "a==b" << endl;
    }
    else {
        cout << "a!=b" << endl;
    }
}



class Person006
{
public:
    Person006(string name, int age)
    {
        this->m_Name = name;
        this->m_Age = age;
    }
    string m_Name;
    int m_Age;
};

//具体化，显示具体化的原型和实现以template<>开头，并通过名称来指出类型
//具体化优先于常规模板
template<> bool mycompare006(Person006& p1, Person006& p2)
{
    if (p1.m_Name == p2.m_Name && p1.m_Age == p2.m_Age)
    {
        return true;
    }
    else
    {
        return false;
    }
}
void test00602()
{
    Person006 p1("Tom", 10);
    Person006 p2("Tom", 10);
    //自定义数据类型，不会调用普通的函数模板
    //可以创建具体化的Person数据类型的模板，用于特殊处理这个类型
    bool ret = mycompare006(p1, p2);
    if (ret)
    {
        cout << "p1 == p2 " << endl;
    }
    else
    {
        cout << "p1 != p2 " << endl;
    }
}
